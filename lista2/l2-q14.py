# 14 - Maior Menor
'''Dados dois valores inteiros, o programa deve identificar quem é o maior e quem é o menor'''

valor1 = int(input())
valor2 = int(input())

if valor1 > valor2:
    print('maior:', valor1)
    print('menor:', valor2)
else:
    print('maior:', valor2)
    print('menor:', valor1)